export async function getAttendanceLogsByUserController(
  req: Request,
  res: Response,
) {
  try {
    if (!req.authUser) {
      return res.status(HTTP_STATUS.UNAUTHORIZED).json({
        message: USER_MESSAGES.INVALID_ACCESS_TOKEN,
      });
    }

    const userIdParam = req.params.userId;
    if (!userIdParam || typeof userIdParam !== "string") {
      return res
        .status(HTTP_STATUS.BAD_REQUEST)
        .json({ message: "Invalid userId parameter" });
    }

    const userId = parseInt(userIdParam, 10);
    if (Number.isNaN(userId)) {
      return res
        .status(HTTP_STATUS.BAD_REQUEST)
        .json({ message: "Invalid userId parameter" });
    }

    const result = await getAttendanceLogsService(
      req.authUser.userId,
      req.authUser.roleId,
      userId,
    );

    if (!result.ok) {
      return res.status(result.status).json({ message: result.message });
    }

    return res.status(result.status).json(result.data);
  } catch (error) {
    return res.status(HTTP_STATUS.INTERNAL_SERVER_ERROR).json({
      message: SERVER_MESSAGES.INTERNAL_SERVER_ERROR,
      error,
    });
  }
}
